<?php
namespace App;
use PDO;

class Memos
{
  private $pdo;

  public function __construct()
  {
    $dbUserName = 'root';
    $dbPassword = 'password';
    $dbInfo = 'mysql:host=mysql; dbname=memo; charset=utf8';
    $pdo = new PDO($dbInfo, $dbUserName, $dbPassword);
    $this->pdo = $pdo;
  }

  public function create(string $title, string $email, string $content): void
  {
      $stmt = $this->pdo->prepare(
          'INSERT INTO memo (title, content) VALUES (:title, :content)'
      );
      $stmt->bindParam(':title', $title, PDO::PARAM_STR);
      $stmt->bindParam(':content', $content, PDO::PARAM_STR);
      $stmt->execute();
  }

  //contactsテーブル取得
  public function get_Memos()
  {
    $stmt ="SELECT
    title,
    content
    FROM pages";
    $statement = $this->pdo->prepare($stmt);
    $statement->execute();
  }

}

?> 
